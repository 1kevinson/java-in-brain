package io.java.codes;

public class Variables {

    int y; // il peut représenter des valeurs comprises entre -2 147 483 648 (-2 ^ 31 ^) et 2 147 483 647 (2 ^ 31 ^ -1).
    byte empty; // Comme la taille de la mémoire est si petite, byte ne peut contenir que les valeurs comprises entre -128 (-2 ^ 7 ^) et 127 (2 ^ 7 ^ - 1).
    short s; // Sa plage de valeurs possibles va de -32 768 (-2 ^ 15 ^) à 32 767 (2 ^ 15 ^ -1).
    long l; // Les valeurs possibles d’un long sont comprises entre -9 223 372 036 854 775 808 (-2 ^ 63 ^) et 9 223 372 036 854 775 807 (2 ^ 63 ^ - 1).
    float f; //La plus petite décimale est 1.40239846 x 10 ^ -45 ^ et la plus grande valeur est 3,40282347 x 10 ^ 38 ^.
    double d; // La plage est 4,9406564584124654 x 10 ^ -324 ^ à 1,7976931348623157 x 10 ^ 308 ^. Cet intervalle peut aussi être positif ou négatif.
    boolean b; //  Il ne peut contenir que deux valeurs: true ou false
    char c; // Aussi appelé caractère, char est un entier de 16 bits représentant un caractère codé en Unicode. Sa plage va de 0 à 65 535.
}

